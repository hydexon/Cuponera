package sv.edu.udb.cuponera.pojo;

public class Rol {
	private int id;
	private String rol;
	private int rolVal;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRol() {
		return rol;
	}
	public void setRol(String rol) {
		this.rol = rol;
	}
	public int getRolVal() {
		return rolVal;
	}
	public void setRolVal(int rolVal) {
		this.rolVal = rolVal;
	}

	
}
